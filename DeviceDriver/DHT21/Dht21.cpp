DigitalInOut DHT21IO(PD_11,PIN_OUTPUT,PullNone,1);
void DHT21_Rst(void)	   
{                 
		DHT21_IO_OUT(); 	//SET OUTPUT
		DHT21_DQ_OUT=0; 	//拉低DQ
		wait_ms(20);    	//拉低至少18ms
		DHT21_DQ_OUT=1; 	//DQ=1 
		wait_us(30);     	//主机拉高20~40us
}
//等待DHT21的回应
//返回1:未检测到DHT21的存在
//返回0:存在
uint8_t DHT21_Check(void) 	   
{   
	uint8_t retry=0;
	DHT21_IO_IN();//SET INPUT	 
    while (DHT21_DQ_IN&&retry<100)//DHT21会拉低40~80us
	{
		retry++;
		wait_us(1);
	};	 
	if(retry>=100)return 1;
	else retry=0;
    while (!DHT21_DQ_IN&&retry<100)//DHT21拉低后会再次拉高40~80us
	{
		retry++;
		wait_us(1);
	};
	if(retry>=100)return 1;	    
	return 0;
}
//从DHT21读取一个位
//返回值：1/0
uint8_t DHT21_Read_Bit(void) 			 
{
 	uint8_t retry=0;
	while(DHT21_DQ_IN&&retry<100)//等待变为低电平
	{
		retry++;
		wait_us(1);
	}
	retry=0;
	while(!DHT21_DQ_IN&&retry<100)//等待变高电平
	{
		retry++;
		wait_us(1);
	}
	wait_us(40);//等待40us
	if(DHT21_DQ_IN)return 1;
	else return 0;		   
}
//从DHT21读取一个字节
//返回值：读到的数据
uint8_t DHT21_Read_Byte(void)    
{        
    uint8_t i,dat;
    dat=0;
	for (i=0;i<8;i++) 
	{
   		dat<<=1; 
	    dat|=DHT21_Read_Bit();
    }						    
    return dat;
}
//从DHT21读取一次数据
//temp:温度值(范围:0~50°)
//humi:湿度值(范围:20%~90%)
//返回值：0,正常;1,读取失败
uint8_t DHT21_Read_Data(float *temp,float *humi)    
{        
 	uint8_t buf[5];
	uint8_t i;
	DHT21_Rst();
	if(DHT21_Check()==0)
	{
		for(i=0;i<5;i++)//读取40位数据
		{
			buf[i]=DHT21_Read_Byte();
		}
			//printf(" buf0-4: %x %x %x %x %x\r\n",buf[0],buf[1],buf[2],buf[3],buf[4]);
		if(((buf[0]+buf[1]+buf[2]+buf[3])&0xff)==buf[4])
		{
		
			//printf("T:%d H:%d\r\n",buf[0]*256+buf[1],buf[2]*256+buf[3]);
		
			*humi=((buf[0]<<8) + buf[1])*0.1;
			if(buf[2]&0x80){
				*temp=(((buf[2]&0x7f)<<8) + buf[3])*(-1)*(0.1);
			}
			else{
				*temp=((buf[2]<<8)+buf[3])*(0.1);
			}
			
		}
	}else return 1;
	return 0;	    
}
